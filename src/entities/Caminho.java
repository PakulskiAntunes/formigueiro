package entities;

public class Caminho {
    public int posX;
    public int posY;

    public Caminho(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }
    
}
